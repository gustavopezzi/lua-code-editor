                    <span>Invitation</span><br/>
                    We would like to invite you to take part in a research study. Before you decide whether you would like to take part it is important that you understand why the research is being done and what it would involve for you. Please take time to read the following information carefully and discuss it with others if you wish. Ask us if there is anything that is not clear or if you would like more information.
                </li>
                <li>
                    <span>What is the purpose of this study?</span><br/>
                    This research project aims to investigate how teaching game programming can help students to understand the applications of high-school level mathematics and physics concepts.
                </li>
                <li>
                    <span>Why have I been invited?</span><br/>
                    You have been chosen because, as a former high-school student, you have the necessary background and knowledge required to take part in the project’s method of evaluation.
                </li>
                <li>
                    <span>Do I have to take part?</span><br/>
                    It is up to you to decide whether or not to take part. If you do decide to take part, you will be asked to sign a consent form. If you decide to take part, you are still free to withdraw at any time and without giving a reason. It is important to notice that, once the data has been anonymized and published, you will no longer be able to withdraw your data.
                </li>
                <li>
                    <span>What will happen if I take part?</span><br/>
                    You will take part on a session which involves answering a few questions about high-school level mathematics for 15 minutes, following by 30 minutes of activities, which include playing a game and completing the game proposed tasks, and finalizing with a 15-minute questionnaire that will assess the benefits of the activity and your impressions about the method used. The experiment will last no more than 1 hour, and you will be notified when the research data is published and becomes available.
                </li>
                <li>
                    <span>What do I have to do?</span><br/>
                    You will answer a set of questions, play a game, complete the game proposed tasks, and answer a questionnaire about the game and about your impressions about the method used.
                </li>
                <li>
                    <span>What are the possible disadvantages and risk of taking part?</span><br/>
                    Participating in the research is not anticipated to cause you any disadvantages or discomfort. The potential physical and/or psychological harm or distress will be the same as any experienced in everyday life.
                </li>
                <li>
                    <span>What are the possible benefits of taking part?</span><br/>
                    Whilst there are no immediate benefits for those people participating in the project, it is hoped that this work will become part of a larger project that will be used by schools and educators as a tool to improve the quality of teaching mathematics and physics.
                </li>
                <li>
                    <span>What will happen when the research study stops?</span><br/>
                    Should the research stop earlier than planned and you are affected in any way we will tell you and explain why.
                </li>
                <li>
                    <span>Will my taking part in the study be kept confidential?</span><br/>
                    All the information that we collect about you during the course of the research will be kept strictly confidential. You will not be able to be identified or identifiable in any reports or publications. Any data collected about you in the online questionnaire will be stored online in a form protected by passwords and other relevant security processes and technologies.
                </li>
                <li>
                    <span>What should I do if I want to take part?</span><br/>
                    You can contact the main researchers or City University of London directly and inform your wish to take part in this research study.
                </li>
